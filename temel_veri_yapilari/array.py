# Array oluşturma
arr = [10, 20, 30, 40, 50]

# Elemanlara erişim
print(arr[0]) # 10
print(arr[3]) # 40

# Eleman değiştirme
arr[2] = 35
print(arr) # [10, 20, 35, 40, 50]

# Eleman ekleme
arr.append(60)
print(arr) # [10, 20, 35, 40, 50, 60]

arr.pop(1) # 1.indeksteki elemanı siler (20)
print(arr) # [10, 35, 40, 50, 60]

